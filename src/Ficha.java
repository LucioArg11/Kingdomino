
public class Ficha {

	private int numero;
	private Paisaje izquierda;
	private Paisaje derecha;

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public Paisaje getIzquierda() {
		return izquierda;
	}

	public void setIzquierda(Paisaje izquierda) {
		this.izquierda = izquierda;
	}

	public Paisaje getDerecha() {
		return derecha;
	}

	public void setDerecha(Paisaje derecha) {
		this.derecha = derecha;
	}

}
